#include "push_swap.h"

int	is_valid_number(char *str)
{
	int		i;
	long	value;

	i = 0;
	if (str[i] == '-' || str[i] == '+')
		i++;
	if (!str[i])
		return (0);
	while (str[i])
	{
		if (str[i] < '0' || str[i] > '9')
			return (0);
		i++;
	}
	value = ft_atol(str);
	if (value < INT_MIN || value > INT_MAX)
		return (0);
	return (1);
}

int	is_duplicate(t_stack *stack, int value)
{
	t_node	*current;

	current = stack->top;
	while (current)
	{
		if (current->value == value)
			return (1);
		current = current->next;
	}
	return (0);
}
